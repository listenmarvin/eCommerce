import React, { useState } from "react";

export default function WishList({ wishcart, addToCart }) {
  const [wishCART, setWish] = useState(wishcart);

  const removeFromCart = (id) => {
    const newList = wishCART.filter((l) => l.id !== id);
    setWish(newList);
  };

  return (
    <div className="flex">
      {wishCART?.map((wishItem, index) => {
        return (
          <div className="product_item">
            <img src={wishItem.img} alt="product image" />
            <p>
              <b>Title :</b>
              {wishItem.title}
            </p>
            <p>
              <b>Author :</b>
              {wishItem.author}
            </p>
            <p>
              <b>Price :</b>
              {wishItem.price}
            </p>
            <button
              className="btn btn-primary"
              onClick={() => addToCart(wishItem)}
            >
              Add To Cart{" "}
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width="16"
                height="16"
                fill="currentColor"
                className="bi bi-cart-fill"
                viewBox="0 0 16 16"
              >
                <path d="M0 1.5A.5.5 0 0 1 .5 1H2a.5.5 0 0 1 .485.379L2.89 3H14.5a.5.5 0 0 1 .491.592l-1.5 8A.5.5 0 0 1 13 12H4a.5.5 0 0 1-.491-.408L2.01 3.607 1.61 2H.5a.5.5 0 0 1-.5-.5zM5 12a2 2 0 1 0 0 4 2 2 0 0 0 0-4zm7 0a2 2 0 1 0 0 4 2 2 0 0 0 0-4zm-7 1a1 1 0 1 1 0 2 1 1 0 0 1 0-2zm7 0a1 1 0 1 1 0 2 1 1 0 0 1 0-2z" />
              </svg>
            </button>
            <button
              className="btn btn-dark"
              onClick={() => removeFromCart(wishItem.id)}
            >
              {" "}
              Remove from wishlist{" "}
              <svg
                xmlns="http://www.w3.org/2000/svg"
                width="16"
                height="16"
                fill="currentColor"
                className="bi bi-trash3-fill"
                viewBox="0 0 16 16"
              >
                <path d="M11 1.5v1h3.5a.5.5 0 0 1 0 1h-.538l-.853 10.66A2 2 0 0 1 11.115 16h-6.23a2 2 0 0 1-1.994-1.84L2.038 3.5H1.5a.5.5 0 0 1 0-1H5v-1A1.5 1.5 0 0 1 6.5 0h3A1.5 1.5 0 0 1 11 1.5Zm-5 0v1h4v-1a.5.5 0 0 0-.5-.5h-3a.5.5 0 0 0-.5.5ZM4.5 5.029l.5 8.5a.5.5 0 1 0 .998-.06l-.5-8.5a.5.5 0 1 0-.998.06Zm6.53-.528a.5.5 0 0 0-.528.47l-.5 8.5a.5.5 0 0 0 .998.058l.5-8.5a.5.5 0 0 0-.47-.528ZM8 4.5a.5.5 0 0 0-.5.5v8.5a.5.5 0 0 0 1 0V5a.5.5 0 0 0-.5-.5Z" />
              </svg>
            </button>
          </div>
        );
      })}
    </div>
  );
}
